// Calculate the tip amount of a food bill given the tip percentage 
// tips are subject to service 


let foodBill = 120; // Food bill, value can be changed 

let tip = 0.15 * foodBill; //decimal version of percentage, value to change. 

console.log("The tip on a $" + foodBill + " food bill is $" + tip); //telling tip on bill 

const totalFoodBill = (tip + foodBill);  //formula for the entire bill
console.log( "the entire bill is $" +  totalFoodBill); //entire bill 